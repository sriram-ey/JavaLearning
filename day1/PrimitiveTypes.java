package day1;

public class PrimitiveTypes {
	static boolean bool;
	static double doubl;
	static float flo;
	static int in;
	static long lon;
	static String str;

	public static void main(String[] args) {
		System.out.println("Default values of boolean = " + bool);
		System.out.println("Default values of double " + doubl);
		System.out.println("Default values of float " + flo);
		System.out.println("Default values of int " + in);
		System.out.println("Default values of long " + lon);
		System.out.println("Default values of String " + str);

	}
}
